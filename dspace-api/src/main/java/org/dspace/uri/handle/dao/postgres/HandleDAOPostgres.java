package org.dspace.uri.handle.dao.postgres;

import org.dspace.core.Context;
import org.dspace.storage.rdbms.DatabaseManager;
import org.dspace.storage.rdbms.TableRow;
import org.dspace.storage.rdbms.TableRowIterator;
import org.dspace.uri.handle.dao.HandleDAO;
import org.dspace.uri.handle.dao.HandleStorageException;

import java.sql.SQLException;

/**
 * Implementation of HandleDAO to support storage using PostgreSQL
 *
 * @author Richard Jones
 */
public class HandleDAOPostgres extends HandleDAO
{
    /** SQL to get the next handle from the sequence table */
    private String nextSQL = "SELECT getnextid('handle') AS hdl";

    /**
     * Construct a new instance of HandleDAOPostgres
     *
     * @param context
     */
    public HandleDAOPostgres(Context context)
    {
        super(context);
    }

    /**
     * Generate the next handle value to be assigned.
     *
     * This is generated by requesting the next value from a sequence in the database.
     *
     * @return
     */
    public int getNextHandle()
            throws HandleStorageException
    {
        try
        {
            TableRowIterator tri = DatabaseManager.query(context, nextSQL);
            if (!tri.hasNext())
            {
                throw new HandleStorageException("Unable to generate new handle");
            }
            TableRow row = tri.next();
            int handle = row.getIntColumn("hdl");
            tri.close();
            return handle;
        }
        catch (SQLException e)
        {
            throw new RuntimeException(e);
        }
    }
}
